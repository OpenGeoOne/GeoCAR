    def initAlgorithm(self, config=None):
        # Define the folder destination parameter
        self.addParameter(
            QgsProcessingParameterFolderDestination(
                self.OUTPUT_FOLDER,
                'Pasta para gravar os arquivos Zip de Goiás GO(Arquivos para o CAR)'
            )
        )

    def processAlgorithm(self, parameters, context, feedback):
        output_folder = self.parameterAsString(parameters, self.OUTPUT_FOLDER, context)

        # Obtém a camada ATI do projeto
        project = QgsProject.instance()
        camada_ati = project.mapLayersByName("ATI")
        if not camada_ati:
            feedback.reportError("Camada ATI não encontrada no projeto.")
            return {}
        camada_ati = camada_ati[0]

        # Caminho do arquivo da camada ATI
        caminho_ati = camada_ati.source()
        pasta_entrada = os.path.dirname(caminho_ati)

        # Nome do ZIP único
        nomeZipUnico = os.path.join(output_folder, "Camadas_GO.zip")

        # Lista todos os arquivos da pasta de entrada, exceto Linha_Auxiliar.*
        arquivos_para_zip = []
        for root, dirs, files in os.walk(pasta_entrada):
            for file in files:
                if file.lower().endswith((".shp", ".shx", ".dbf", ".prj", ".cpg", ".json", ".qmd", ".qpj")):
                    if 'Linha_Auxiliar' not in file:
                        arquivos_para_zip.append(os.path.join(root, file))

        # Cria o ZIP único
        if arquivos_para_zip:
            with zipfile.ZipFile(nomeZipUnico, 'w') as zipf:
                for f in arquivos_para_zip:
                    zipf.write(f, os.path.basename(f))

        feedback.pushInfo(f"ZIP criado em: {nomeZipUnico}")
        return {}

